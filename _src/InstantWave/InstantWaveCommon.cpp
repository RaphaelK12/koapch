#include "Common.h"

#include "InstantWaveCommon.h"

//////////////////////////////////////////////////////////////////////////////////////////////

void * operator new(size_t theSize) throw()
{
	return HeapAlloc(GetProcessHeap(), 0, theSize);
}

//////////////////////////////////////////////////////////////////////////////////////////////

void operator delete(void * thePtr) throw()
{
	HeapFree(GetProcessHeap(), 0, thePtr);
}